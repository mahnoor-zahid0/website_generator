import prisma from '@/lib/db';

async function getStats() {
    try {
        const [totalTemplates, totalUsers, premiumUsers, totalWebsites] = await Promise.all([
            prisma.template.count(),
            prisma.user.count(),
            prisma.user.count({ where: { role: 'PREMIUM' } }),
            prisma.website.count(),
        ]);

        return {
            totalTemplates,
            totalUsers,
            premiumUsers,
            totalWebsites,
        };
    } catch (error) {
        console.error('Error fetching stats:', error);
        return {
            totalTemplates: 0,
            totalUsers: 0,
            premiumUsers: 0,
            totalWebsites: 0,
        };
    }
}

